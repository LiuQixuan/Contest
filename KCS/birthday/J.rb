#!/usr/bin/env ruby

expr = gets.chomp

field = <<FIELD
.....|.....
.....|.....
.....|.....
.....|.....
.....|.....
-----+-----
.....|.....
.....|.....
.....|.....
.....|.....
.....|.....
FIELD
field = field.each_line.to_a
(-5..5).each do |x|
  y = eval(expr)
  if y.abs <= 5
    field[5-y][x+5] = "*"
  end
end
puts field
